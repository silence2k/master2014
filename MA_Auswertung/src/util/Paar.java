package util;

public class Paar {
	
	private int gewicht;
	private int anzahl;
	
	public Paar(int gewicht, int anzahl) {
		super();
		this.gewicht = gewicht;
		this.anzahl = anzahl;
	}

	public int getGewicht() {
		return gewicht;
	}

	public int getAnzahl() {
		return anzahl;
	}

	

}
